//split
var courses="java python javascript node"
console.log(courses) 
console.log(courses.split(" "))     //[ 'java', 'python', 'javascript', 'node' ]

//join()
var courses=[ 'java', 'python', 'javascript', 'node' ]
console.log(courses)
console.log(courses.join(" "))      //java python javascript node